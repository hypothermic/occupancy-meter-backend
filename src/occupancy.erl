-module(occupancy).

-export([
	start/0,
	stop/0
]).

%%% =================================================================
%%% Geexporteerde functies
%%% =================================================================

% -----------------------------------------------------------------------------
% start
%
% 	Deze functie start de occupancy applicatie
% -----------------------------------------------------------------------------

start() ->
	application:start(occupancy).

% -----------------------------------------------------------------------------
% stop
%
% 	Deze functie stopt de occupancy applicatie
% -----------------------------------------------------------------------------

stop() ->
	application:stop(occupancy).